	<h3>The start..</h3>
	<p>It all started back in 2002, when the students of <a href="http://www.hig.no">Gj√∏vik University College</a> had to pick their main project for the last semester of school. 
	The first concept was to make a similar game to what we have now, but instead base it on the Wheel of Time books. After a few emails (not actually reaching Robert Jordan) with Glen Dahlgren (project leader of Legend entertainment) they came to the conclusion that copyright issues were tricky, so decided to make their "own" setting.
	</p>

	<h3>Chaos is born</h3>
	<p>The original game had 1 attack type, 3 races, 4 thievery ops, and a few magic ops. No protection time back then or morale (unlimited number of attacks), so they had to reset pretty quickly.
	The second beta didnt have as many bugs, and they were able to correct them while playing. There were nearly 100 players already at this stage.
	The first ages, all, unfortunatly had bugs in them. New things and features that "almost" worked were added and corrected. It was still a success, almost doubling in size each age.</p>

	<h3>Chaos is reborn?</h3>
	<p>After a server crash, and the seemingly inevitable end of the game, it did come back online eventually. Anders tweaked the scripts, ordered www.tkoc.net as a domain, and took up the phone and started calling the other creators. They sent out emails to previous players, and the ages restarted with 50-100 loyal players.
	</p>

	<h3>A new look</h3>
	<p>Christian would prove to be very resourceful, and he had plenty of ideas on how chaos should look. It was an incredible new look, changing the whole feeling of chaos. Meanwhile, a new race (dwarves) was added, new attacks, new spells.. The council, the forum. Almost everything got a much needed overhaul. (the world forum would, unfortunalty be painfully slow, together with a few more new bugs)</p>

	<h3>Before the break</h3>
	<p>Everything looked promising. There were over 400 players, and it looked on the rise. However, at around age 15 a slow decline started to happen with admin inactivity and people leaving the game. This decline took much more severe forms in 2008 when the player base eventually reduced to a mere 60 in August. The decision was made to stop Chaos. The source code was put online to make an eventual restart possile, albeit by different people.</p>

<h3>A Restart!</h3>
	<p>Which is exactly what happened. A full year later, in 2009, Tasos decided to restart the project and he called for the aid of Peter. Together they got the stalled wheel going again and with the approval of the old Admins, The Kingdoms of Chaos are once again a battleground of fun for many! A new forum, better login systems, many bug fixes and many more ongoing changes and updates in the past few months (as of January 2010) alone! Enjoy the revamped game, and help us in making it a great one!</p>

